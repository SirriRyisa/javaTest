public class SwitchCase{
  //This class will have a dynmic method that can make use of class instances.
  
  public static void main(String[] args){
    int day = 7;

    switch(day) {
      case 1:
      System.out.println("I like monday");
      break;
      case 2:
      System.out.println("Don't get me wrong, saturdays are my best days");
      break;
      case 7:
      System.out.println("Yeeeeee!!! Sundays are for famlilies");
      break;
    }
  }
}
